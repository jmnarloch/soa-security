package org.soa.security.core.http;

/**
 * <p>Represents the http method.</p>
 *
 * @author Jakub Narloch (jmnarloch@gmail.com)
 * @version 1.0
 */
public enum HttpMethod {

    /**
     * <p>Represents the PUT http method.</p>
     */
    PUT,

    /**
     * <p>Represents the POST http method.</p>
     */
    POST,

    /**
     * <p>Represents the GET http method.</p>
     */
    GET,

    /**
     * <p>Represents the DELETE http method.</p>
     */
    DELETE
}
